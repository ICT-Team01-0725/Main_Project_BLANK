CREATE TABLE user_db.user_info_tbl (
    user_idx INT AUTO_INCREMENT PRIMARY KEY,
    user_id VARCHAR(250) NOT NULL,
    user_level_idx INT NOT NULL,
    user_pw VARCHAR(100) DEFAULT NULL,
    user_profile VARCHAR(255) DEFAULT NULL,
    user_profile_name VARCHAR(255) DEFAULT NULL,
    user_reg_date DATE NOT NULL DEFAULT '2000-01-01',
    user_name VARCHAR(50) NOT NULL,
    user_nickname VARCHAR(50) NOT NULL,
    user_kakao VARCHAR(20) DEFAULT NULL,
    user_naver VARCHAR(100) DEFAULT NULL,
    user_google VARCHAR(225) DEFAULT NULL,
    user_birth_date DATE DEFAULT NULL,
    user_gender ENUM('Male', 'Female', 'Other', 'Etc') DEFAULT 'Etc',
    user_email VARCHAR(255) NOT NULL,
    user_phone VARCHAR(20) DEFAULT NULL,
    admin_idx INT DEFAULT NULL,
    user_out_date DATE DEFAULT NULL,
    user_out_reason VARCHAR(100) DEFAULT NULL,
    user_last_login DATETIME DEFAULT NULL,
    CONSTRAINT fk_user_level FOREIGN KEY (user_level_idx) REFERENCES user_db.user_level_tbl(user_level_idx),
    CONSTRAINT fk_user_admin FOREIGN KEY (admin_idx) REFERENCES admin_db.admin_info_tbl(admin_idx)
);
